class Log
{
	public static function notice($where, $message)
	{
		$fp = fopen('files/log.txt', 'a+');
		fwrite($fp, date('Y-m-d H:m:s').' :: '.$where.' :: '.$message."\n");
		fclose($fp);
	}
}
